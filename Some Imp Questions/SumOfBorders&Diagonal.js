
let arr = [
    [1, 2, 3, 4, 1,],        //[00, 01, 02, 03, 04]
    [5, 6, 7, 8, 2],        //[10, 11, 12, 13, 14]
    [9, 10, 11, 12, 13],    //[20, 21, 22, 23, 24]
    [13, 14, 15, 16, 15],   //[30, 31, 32, 33, 34]
    [11, 12, 15, 19, 15],   //[40, 41, 42, 43, 44]
];

let n = arr.length;
let sum = 0;
for(let i = 0; i<n; i++){
    for(let j = 0; j<n; j++){
          if(i==0 || i==n-1 || j==0 || j==n-1){
                sum+= arr[i][j]
          }
          else if(i==j || i+j== n-1){
                sum+= arr[i][j]
          }
    }
}
